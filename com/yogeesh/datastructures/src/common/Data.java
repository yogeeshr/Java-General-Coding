public class Data {

	Integer value = 10;

	/**
	 * @param value
	 */
	public Data(Integer value) {
		super();
		this.value = value;
	}

	/**
	 * @return the data
	 */
	public Integer getValue() {
		return value;
	}

	/**
	 * @param value the data to set
	 */
	public void setValue(Integer value) {
		this.value = value;
	}
			
}
